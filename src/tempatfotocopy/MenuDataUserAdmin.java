    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNamaUser.getText().trim();
    String username = txtUsername.getText().trim();
    String password = txtPassword.getText().trim();
    String role = cmbRole.getSelectedItem().toString();

    if (nama.isEmpty()) {
        JOptionPane.showMessageDialog(null,
                "Nama user tidak boleh kosong");
        return;
    }

    if (username.isEmpty()) {
        JOptionPane.showMessageDialog(null,
                "Username tidak boleh kosong");
        return;
    }

    if (password.isEmpty()) {
        JOptionPane.showMessageDialog(null,
                "Password tidak boleh kosong");
        return;
    }

    // VALIDASI ROLE
    if (role.equals("===Pilih Role===")) {
        JOptionPane.showMessageDialog(null,
                "Silakan pilih role terlebih dahulu");
        return;
    }

    try {
        Connection conn = TempatFotocopy.getKoneksi();

        // CEK USERNAME
        String cekSql =
                "SELECT * FROM user WHERE username=?";

        PreparedStatement cek =
                conn.prepareStatement(cekSql);

        cek.setString(1, username);

        ResultSet rs = cek.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(null,
                    "Username sudah digunakan");
            return;
        }

        // SIMPAN USER KE DATABASE
        String sql =
                "INSERT INTO user "
                + "(nama_user, username, password, level) "
                + "VALUES (?, ?, ?, ?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, nama);
        pst.setString(2, username);
        pst.setString(3, password);
        pst.setString(4, role);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null,
                "User berhasil disimpan dan sudah bisa login");

        tampilData();
        kosongkanForm();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,
                "Error : " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

