    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         if (txtIdUser.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(
            this,
            "Pilih data user dari tabel terlebih dahulu!"
        );
        return;
    }

    String nama = txtNamaUser.getText().trim();
    String username = txtUsername.getText().trim();
    String password = txtPassword.getText().trim();
    String role = cmbRole.getSelectedItem().toString();

    if (nama.isEmpty() || username.isEmpty()) {
        JOptionPane.showMessageDialog(
            this,
            "Nama dan username harus diisi!"
        );
        return;
    }

    try {
        PreparedStatement pst;

        if (password.isEmpty()) {

            String sql = "UPDATE user SET "
                       + "nama_user = ?, "
                       + "username = ?, "
                       + "level = ? "
                       + "WHERE id_user = ?";

            pst = conn.prepareStatement(sql);

            pst.setString(1, nama);
            pst.setString(2, username);
            pst.setString(3, role);
            pst.setInt(
                4,
                Integer.parseInt(txtIdUser.getText())
            );

        } else {

            String sql = "UPDATE user SET "
                       + "nama_user = ?, "
                       + "username = ?, "
                       + "password = ?, "
                       + "level = ? "
                       + "WHERE id_user = ?";

            pst = conn.prepareStatement(sql);

            pst.setString(1, nama);
            pst.setString(2, username);
            pst.setString(3, password);
            pst.setString(4, role);
            pst.setInt(
                5,
                Integer.parseInt(txtIdUser.getText())
            );
        }

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
            this,
            "Data user berhasil diubah!"
        );

        tampilData();
        kosongkanForm();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(
            this,
            "Gagal mengubah user: " + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnEditActionPerformed

