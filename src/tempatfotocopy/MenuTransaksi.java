private void tampilLayanan() {

    try {

        Connection conn = TempatFotocopy.getKoneksi();

        String sql = "SELECT nama_layanan FROM layanan ORDER BY nama_layanan";

        PreparedStatement ps = conn.prepareStatement(sql);

        ResultSet rs = ps.executeQuery();

        cmbLayanan.removeAllItems();

        while (rs.next()) {

            cmbLayanan.addItem(rs.getString("nama_layanan"));

        }

        rs.close();
        ps.close();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }

}

