    private void btnSImpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSImpanActionPerformed
        // TODO add your handling code here:
        String tanggal = txtTanggal.getText().trim();
        String idPelanggan = txtIdPelanggan.getText().trim();
        String totalHarga = txtTotalHarga.getText().trim();

    // CEK SESSION USER
    if (Session.idUser <= 0) {
        JOptionPane.showMessageDialog(
            null,
            "ID User tidak ditemukan. Silakan login ulang."
        );
        return;
    }

    // CEK ID PELANGGAN
    if (idPelanggan.isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "ID Pelanggan harus diisi"
        );
        return;
    }

    // CEK TOTAL HARGA
    if (totalHarga.isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "Total Harga harus diisi"
        );
        return;
    }

    try {

        int idPelangganInt =
            Integer.parseInt(idPelanggan);

        double total =
            Double.parseDouble(totalHarga);

        Connection conn =
            TempatFotocopy.getKoneksi();

        String cekSql =
            "SELECT id_pelanggan, nama_pelanggan "
            + "FROM pelanggan "
            + "WHERE id_pelanggan = ?";

        PreparedStatement cek =
            conn.prepareStatement(cekSql);

        cek.setInt(1, idPelangganInt);

        ResultSet rs =
            cek.executeQuery();

        // JIKA ID PELANGGAN TIDAK ADA
        if (!rs.next()) {

            JOptionPane.showMessageDialog(
                null,
                "ID Pelanggan " + idPelangganInt
                + " tidak ditemukan di Data Pelanggan"
            );

            rs.close();
            cek.close();
            return;
        }

        // Ambil nama pelanggan
        String namaPelanggan =
            rs.getString("nama_pelanggan");

        rs.close();
        cek.close();

        String sql =
            "INSERT INTO transaksi "
            + "(tanggal, id_user, "
            + "id_pelanggan, total_harga) "
            + "VALUES (?, ?, ?, ?)";

        PreparedStatement pst =
            conn.prepareStatement(sql);

        pst.setDate(
            1,
            java.sql.Date.valueOf(tanggal)
        );

        // ID USER OTOMATIS DARI LOGIN
        pst.setInt(
            2,
            Session.idUser
        );

        // ID PELANGGAN DARI DATA PELANGGAN
        pst.setInt(
            3,
            idPelangganInt
        );

        pst.setDouble(
            4,
            total
        );

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
            null,
            "Transaksi berhasil disimpan\n"
            + "Pelanggan: " + namaPelanggan
        );

        tampilData();
        kosongkanForm();

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
            null,
            "ID Pelanggan dan Total Harga harus berupa angka"
        );

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
            null,
            "Error menyimpan transaksi: "
            + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnSImpanActionPerformed

