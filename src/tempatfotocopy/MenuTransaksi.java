    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (idTransaksiTerpilih == -1) {

        JOptionPane.showMessageDialog(
                null,
                "Pilih transaksi dari tabel"
        );

        return;
    }

    String tanggal =
            txtTanggal.getText().trim();

    String idUser =
            txtIdUser.getText().trim();

    String idPelanggan =
            txtIdPelanggan.getText().trim();

    String totalHarga =
            txtTotalHarga.getText().trim();

    if (idUser.isEmpty()
            || idPelanggan.isEmpty()
            || totalHarga.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Semua data harus diisi"
        );

        return;
    }

    try {

        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "UPDATE transaksi SET "
                + "tanggal = ?, "
                + "id_user = ?, "
                + "id_pelanggan = ?, "
                + "total_harga = ? "
                + "WHERE id_transaksi = ?";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setDate(
                1,
                Date.valueOf(tanggal)
        );

        pst.setInt(
                2,
                Integer.parseInt(idUser)
        );

        pst.setInt(
                3,
                Integer.parseInt(idPelanggan)
        );

        pst.setDouble(
                4,
                Double.parseDouble(totalHarga)
        );

        pst.setInt(
                5,
                idTransaksiTerpilih
        );

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
                null,
                "Transaksi berhasil diubah"
        );

        tampilData();
        kosongkanForm();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error mengubah transaksi: "
                + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
         if (idTransaksiTerpilih == -1) {

        JOptionPane.showMessageDialog(
                null,
                "Pilih transaksi dari tabel"
        );

        return;
    }

    int jawab =
            JOptionPane.showConfirmDialog(
                null,
                "Yakin ingin menghapus transaksi ini?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
            );

    if (jawab == JOptionPane.YES_OPTION) {

        try {

            Connection conn =
                    TempatFotocopy.getKoneksi();

            String sql =
                    "DELETE FROM transaksi "
                    + "WHERE id_transaksi = ?";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setInt(
                    1,
                    idTransaksiTerpilih
            );

            pst.executeUpdate();
            pst.close();

            JOptionPane.showMessageDialog(
                    null,
                    "Transaksi berhasil dihapus"
            );

            tampilData();
            kosongkanForm();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Error menghapus transaksi: "
                    + e.getMessage()
            );
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

