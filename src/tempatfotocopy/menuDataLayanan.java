    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         if (idLayananTerpilih == -1) {

        JOptionPane.showMessageDialog(
                null,
                "Pilih data layanan dari tabel"
        );
        return;
    }

    String nama =
            txtNamaLayanan.getText().trim();

    String harga =
            txtHarga.getText().trim();

    String satuan =
            txtSatuan.getText().trim();

    if (nama.isEmpty()
            || harga.isEmpty()
            || satuan.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Semua data harus diisi"
        );
        return;
    }

    try {

        double hargaDouble =
                Double.parseDouble(harga);

        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "UPDATE layanan SET "
                + "nama_layanan = ?, "
                + "harga = ?, "
                + "satuan = ? "
                + "WHERE id_layanan = ?";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, nama);
        pst.setDouble(2, hargaDouble);
        pst.setString(3, satuan);
        pst.setInt(4, idLayananTerpilih);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
                null,
                "Data layanan berhasil diubah"
        );

        tampilData();
        kosongkanForm();

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                null,
                "Harga harus berupa angka"
        );

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error mengubah layanan: "
                + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnEditActionPerformed

