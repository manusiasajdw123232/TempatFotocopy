    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama =
            txtNamaLayanan.getText().trim();

    String harga =
            txtHarga.getText().trim();

    String satuan =
            txtSatuan.getText().trim();

    if (nama.isEmpty()) {
        JOptionPane.showMessageDialog(
                null,
                "Nama layanan harus diisi"
        );
        return;
    }

    if (harga.isEmpty()) {
        JOptionPane.showMessageDialog(
                null,
                "Harga harus diisi"
        );
        return;
    }

    if (satuan.isEmpty()) {
        JOptionPane.showMessageDialog(
                null,
                "Satuan harus diisi"
        );
        return;
    }

    try {

        double hargaDouble =
                Double.parseDouble(harga);

        if (hargaDouble <= 0) {
            JOptionPane.showMessageDialog(
                    null,
                    "Harga harus lebih dari 0"
            );
            return;
        }

        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "INSERT INTO layanan "
                + "(nama_layanan, harga, satuan) "
                + "VALUES (?, ?, ?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, nama);
        pst.setDouble(2, hargaDouble);
        pst.setString(3, satuan);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
                null,
                "Data layanan berhasil disimpan"
        );

        tampilData();
        kosongkanForm();

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                null,
                "Harga harus berupa angka"
        );

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error menyimpan layanan: "
                + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

