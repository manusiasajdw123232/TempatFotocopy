private void buatTabel() {

    model = new DefaultTableModel();

    model.addColumn("ID Layanan");
    model.addColumn("Nama Layanan");
    model.addColumn("Harga");
    model.addColumn("Satuan");

    tblLayanan.setModel(model);
}
private void tampilData() {

    model.setRowCount(0);

    try {
        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "SELECT id_layanan, nama_layanan, harga, satuan "
                + "FROM layanan "
                + "ORDER BY id_layanan ASC";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_layanan"),
                rs.getString("nama_layanan"),
                rs.getBigDecimal("harga"),
                rs.getString("satuan")
            });
        }

        rs.close();
        pst.close();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error menampilkan data layanan: "
                + e.getMessage()
        );
    }
}
