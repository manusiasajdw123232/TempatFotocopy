    private void btnTampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanActionPerformed
        // TODO add your handling code here:
    String awal =
            txtTanggalAwal.getText().trim();

    String akhir =
            txtTanggalAkhir.getText().trim();

    if (awal.isEmpty() || akhir.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Tanggal awal dan tanggal akhir harus diisi"
        );

        return;
    }

    model.setRowCount(0);

    try {

        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "SELECT t.id_transaksi, t.tanggal, "
                + "p.nama_pelanggan, "
                + "u.nama_user, "
                + "t.total_harga "
                + "FROM transaksi t "
                + "JOIN pelanggan p "
                + "ON t.id_pelanggan = p.id_pelanggan "
                + "JOIN user u "
                + "ON t.id_user = u.id_user "
                + "WHERE DATE(t.tanggal) BETWEEN ? AND ? "
                + "ORDER BY t.tanggal DESC";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, awal);
        pst.setString(2, akhir);

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_transaksi"),
                rs.getDate("tanggal"),
                rs.getString("nama_pelanggan"),
                rs.getString("nama_user"),
                rs.getBigDecimal("total_harga")
            });
        }

        rs.close();
        pst.close();

        if (model.getRowCount() == 0) {

            JOptionPane.showMessageDialog(
                    null,
                    "Tidak ada transaksi pada periode tersebut"
            );
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error filter laporan: "
                + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnTampilkanActionPerformed

