private void tampilLaporan() {

    model.setRowCount(0);

    try {

        Connection conn = TempatFotocopy.getKoneksi();

        String sql =
                "SELECT t.id_transaksi, t.tanggal, "
                + "p.nama_pelanggan, "
                + "u.nama_user, "
                + "t.total_harga "
                + "FROM transaksi t "
                + "JOIN pelanggan p "
                + "ON t.id_pelanggan = p.id_pelanggan "
                + "JOIN user u "
                + "ON t.id_user = u.id_user "
                + "ORDER BY t.id_transaksi DESC";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_transaksi"),
                rs.getDate("tanggal"),
                rs.getString("nama_pelanggan"),
                rs.getString("nama_user"),
                rs.getBigDecimal("total_harga")
            });
        }

        rs.close();
        pst.close();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error menampilkan laporan: "
                + e.getMessage()
        );
    }
}
