private void tampilData() {

    model.setRowCount(0);

    try {

        Connection conn =
                TempatFotocopy.getKoneksi();

        String sql =
                "SELECT p.id_pembayaran, "
                + "p.id_transaksi, "
                + "t.total_harga, "
                + "p.total_bayar, "
                + "p.kembalian, "
                + "p.metode_bayar "
                + "FROM pembayaran p "
                + "JOIN transaksi t "
                + "ON p.id_transaksi = t.id_transaksi "
                + "ORDER BY p.id_pembayaran DESC";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_pembayaran"),
                rs.getInt("id_transaksi"),
                rs.getBigDecimal("total_harga"),
                rs.getBigDecimal("total_bayar"),
                rs.getBigDecimal("kembalian"),
                rs.getString("metode_bayar")
            });
        }

        rs.close();
        pst.close();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error menampilkan pembayaran: "
                + e.getMessage()
        );
    }
}

