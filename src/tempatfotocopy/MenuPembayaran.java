    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
         String idTransaksi =
            txtIdTransaksi.getText().trim();

    String totalTagihan =
            txtTotalTagihan.getText().trim();

    String totalBayar =
            txtTotalBayar.getText().trim();

    String metode =
            cmbMetode.getSelectedItem().toString();

    if (idTransaksi.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "ID transaksi harus diisi"
        );

        return;
    }

    if (totalTagihan.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Tekan ENTER pada ID transaksi "
                + "untuk mengambil total tagihan"
        );

        return;
    }

    if (totalBayar.isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Total bayar harus diisi"
        );

        return;
    }

    if (metode.equals("===Pilih Metode===")) {

        JOptionPane.showMessageDialog(
                null,
                "Silakan pilih metode bayar"
        );

        return;
    }

    try {

        double tagihan =
                Double.parseDouble(totalTagihan);

        double bayar =
                Double.parseDouble(totalBayar);

        if (bayar < tagihan) {

            JOptionPane.showMessageDialog(
                    null,
                    "Uang pembayaran kurang!\n"
                    + "Total Tagihan: " + tagihan + "\n"
                    + "Total Bayar: " + bayar
            );

            return;
        }

        double kembalian =
                bayar - tagihan;

        Connection conn =
                TempatFotocopy.getKoneksi();

        // Cek apakah transaksi sudah dibayar
        String cekSql =
                "SELECT id_pembayaran "
                + "FROM pembayaran "
                + "WHERE id_transaksi = ?";

        PreparedStatement cek =
                conn.prepareStatement(cekSql);

        cek.setInt(
                1,
                Integer.parseInt(idTransaksi)
        );

        ResultSet rs = cek.executeQuery();

        if (rs.next()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Transaksi ini sudah dibayar"
            );

            rs.close();
            cek.close();
            return;
        }

        rs.close();
        cek.close();

        // Simpan pembayaran
        String sql =
                "INSERT INTO pembayaran "
                + "(id_transaksi, total_bayar, "
                + "kembalian, metode_bayar) "
                + "VALUES (?, ?, ?, ?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setInt(
                1,
                Integer.parseInt(idTransaksi)
        );

        pst.setDouble(2, bayar);
        pst.setDouble(3, kembalian);
        pst.setString(4, metode);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(
                null,
                "Pembayaran berhasil disimpan"
        );

        tampilData();
        kosongkanForm();

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                null,
                "ID transaksi dan total bayar "
                + "harus berupa angka"
        );

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error menyimpan pembayaran: "
                + e.getMessage()
        );
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

